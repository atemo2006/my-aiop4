// primitive datatypes
//       string datatypes
// three ways we can identify strings
// backtick
var studentName = `John`;
console.log(typeof(studentName));
// double quote
var studentname2 = "Peter";
console.log(typeof(studentname2));
// single quote
var StudentName3 = 'donald';
console.log(typeof(StudentName3));

//      number datatypes
var age = 20;
console.log(typeof(age));

var price = 5.2;
console.log(typeof(price));

//      boolean datatypes
var iscoding = true;
console.log(typeof(iscoding));

var isCoding = false;
console.log(typeof(isCoding));

// undefined datatype
var job;
console.log(typeof(job));



